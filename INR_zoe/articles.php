  <article class="filterDiv typographie interactif">
    <h3>
      BOIVIN, Diane.  
    </h3>

    <h4>
      <a href="https://etapes.com/diane-boivin-evoque-le-concept-de-lecture-contrariee/" target="_blank">
        Diane Boivin évoque le concept de lecture contrariée, 
      </a>
    </h4> 
    
    <h5>
      étapes:, 2012.
    </h5>
    
    <blockquote cit="https://cargocollective.com/dianeboivin/lecture-relecture">Ma pratique du graphisme s’oriente vers cette notion de l’OULEPO – ouvroir de lectures potentielles – en référence au mouvement littéraire OULIPO.</blockquote>
    
    <p> À l’occasion d’une réédition de Blanche Neige, Diane Boivin se saisit de sa typographie <em>oulepo</em> qui repose ce que l’auteur appelle la lecture contrariée. Ici, les caractères se renversent, s’imbriquent et se retournent, jouant sur l’ambiguïté entre lettre et dessin. L’information est en quelque sorte scénarisée puisque face à cette écriture codée, le lecteur cherche activement les outils pour déchiffrer cette image-texte. Ainsi, le texte en bleu est celui du Blanche Neige, le conte insouciant de la mémoire collective&#8239;; auquel est superposé en rouge l’analyse crue du conte par le psychologue Marc Girard. Si les textes apparaissent de première abord comme illisibles, des calques bleu et rouge permettent de les révéler et de les lires l’un après l’autre.
      
    </p>
   

