    <h3>
      SEDDIKI, Sabrina.  
    </h3>

    <h4>
      <a href="https://www.arteradio.com/son/61664900/comptines_cruelles" target="_blank">
        Comptines cruelles, 
      </a>
    </h4> 
    
    <h5>
      Produit par ARTE Radio, réalisé par Samuel Hirsch, publié le 15/12/2020.
    </h5>
    
    <blockquote cit="#">Cette comptine nous dit que le monde de l'enfance n'est pas si doux. Heureusement, la naiveté enfantine, la candeur de l'enfant, y voit l'émerveillement de la magie.</blockquote>
    
    <p> 
      Du sexe et de la torture. Derrière les histoires irrationnelles des comptines, comme c'est aussi le cas pour les contes de notre enfance, se cache souvent une réalité bien plus féroce qu'il n'y paraît. En prenant pour exemple "Au clair de la Lune", "Une souris verte" ou encore "Gentil Coquelicot", Marie-Claire Bruley, psychologue, souligne dans ce podcast court que ces mélodies entêtantes ont toujours été destinées aux adultes et en expose les origines.
    </p>
   

