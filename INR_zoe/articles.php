    <h3>
      MOREL, Claire.  
    </h3>

    <h4>
      <a href="https://actualitte.com/article/19860/interviews/claire-morel-l-artiste-qui-murmurait-a-l-oreille-des-livres" target="_blank">
        Claire Morel, l'artiste qui murmurait à l'oreille des livres, 
      </a>
    </h4> 
    
    <h5>
      Laure Besnier, 03/05/2018 à 11:06.
    </h5>
    
    <blockquote cit="http://www.metaproject.net/fr/articles/author/152/claire-morel">Je cherchais donc une pièce à faire autour du manuscrit. Et je me suis interrogée : est-ce que dessiner des lettres était un geste plus proche du dessin ou de l’écrit ?</blockquote>
    
    <p> 
      Lorsque le livre est traité non pas pour son contenu mais pour sa matérialité même, est-il encore un livre ? En utilisant le livre comme matière première dans sa création, Claire Morel floute la frontière entre édition et arts plastiques. Plus qu’un produit d’une technologie, elle dit considérer le livre comme un paysage, un espace, une architecture qui se déploie et se dilate. Travaillant sur l’absence, sur ce qui est oublié, Claire Morel laisse souvent de côté l’écrit de l’ouvrage pour se concentrer sur ce qu’est l’essence du livre&#8239;: papier et encre. 
    </p>
   

